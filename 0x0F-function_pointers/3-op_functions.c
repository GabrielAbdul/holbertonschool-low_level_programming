#include "3-calc.h"

/**
 * op_add - function that adds two integers
 *
 * @a: int
 * @b: int
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that subracts two integers
 *
 * @a: int
 * @b: int
 *
 * Return: a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that multiplies two integers
 *
 * @a: int
 * @b: int
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that divides two integers
 *
 * @a: int
 * @b: int
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that modulus' two integers
 *
 * @a: int
 * @b: int
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}

